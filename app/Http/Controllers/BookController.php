    public function store(Request $request)
    {
        $book = Storage::putFile('book',    $request->file('book'));
        $img = Storage::putFile('book-img', $request->file('book-img'));

        Book::create([
            'title' => $request->title, 
            'path'  => $book,
            'img'  => $img,
        ]);


        return redirect('/admin/books');
    }

